public class mixdlinear {
   
        public static void main(String[] args) {
            for (int i = 1; i <= 4; i++) {
                System.out.print(i + " ");
            }
            System.out.println();
            System.out.println(5);
            System.out.println(6);
            for (int i = 10; i >= 7; i--) {
                System.out.print(i + " ");
            }
            System.out.println();
        }
    }
    

